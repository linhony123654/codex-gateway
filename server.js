// Proxy server (Ollama compatible)
function createProxyServer() {
  const proxyApp = express();
  proxyApp.use(express.json({ limit: '10mb' }));

  // Handle pull requests (fake success)
  proxyApp.post('/api/pull', (req, res) => {
    res.setHeader('Content-Type', 'application/x-ndjson');
    res.write(JSON.stringify({ status: 'success' }) + '\n');
    res.write(JSON.stringify({ status: 'complete' }) + '\n');
    res.end();
  });

  // Handle version
  proxyApp.get('/api/version', (req, res) => {
    res.json({ version: '0.135.0' });
  });

  // Handle tags
  proxyApp.get('/api/tags', (req, res) => {
    const config = loadConfig();
    const provider = config.activeProvider ? config.providers[config.activeProvider] : null;
    if (!provider) return res.json({ models: [] });
    res.json({
      models: provider.models.map(m => ({
        name: m,
        model: m,
        modified_at: new Date().toISOString()
      }))
    });
  });

  // Handle models
  proxyApp.get('/v1/models', (req, res) => {
    const config = loadConfig();
    const provider = config.activeProvider ? config.providers[config.activeProvider] : null;
    if (!provider) return res.json({ object: 'list', data: [] });
    res.json({
      object: 'list',
      data: provider.models.map(m => ({
        id: m,
        object: 'model',
        owned_by: config.activeProvider
      }))
    });
  });

  // Handle responses endpoint
  proxyApp.all('/v1/responses', (req, res) => {
    const config = loadConfig();
    const provider = config.activeProvider ? config.providers[config.activeProvider] : null;
    if (!provider) {
      return res.status(502).json({ error: 'No active provider configured' });
    }

    const data = req.body;
    const isStream = data.stream || false;

    // Convert input to messages
    const messages = [];
    if (data.instructions) {
      messages.push({ role: 'system', content: data.instructions });
    }
    for (const item of (data.input || [])) {
      if (typeof item === 'string') {
        messages.push({ role: 'user', content: item });
      } else if (item.role === 'user' || item.role === 'assistant' || item.role === 'developer') {
        let content = item.content || '';
        if (Array.isArray(content)) {
          content = content.map(p => {
            if (p.type === 'input_text') return p.text;
            if (typeof p === 'string') return p;
            return '';
          }).join(' ');
        }
        const role = item.role === 'developer' ? 'system' : item.role;
        messages.push({ role, content });
      }
    }

    // Build chat completions request
    const chatData = {
      model: data.model || provider.models[0],
      messages,
      stream: isStream
    };

    const url = new URL(provider.baseUrl + '/chat/completions');
    const options = {
      hostname: url.hostname,
      port: url.port || 443,
      path: url.pathname,
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${provider.apiKey}`
      }
    };

    const proto = url.protocol === 'https:' ? https : http;
    const proxyReq = proto.request(options, (proxyRes) => {
      if (isStream) {
        res.setHeader('Content-Type', 'text/event-stream');
        res.setHeader('Cache-Control', 'no-cache');
        res.setHeader('Connection', 'keep-alive');

        const responseId = `resp_${Date.now()}`;

        // Send response.created
        res.write(`data: ${JSON.stringify({
          type: 'response.created',
          response: {
            id: responseId,
            object: 'response',
            created_at: Math.floor(Date.now() / 1000),
            model: data.model || provider.models[0],
            output: [],
            status: 'in_progress'
          }
        })}\n\n`);

        // Send output_item.added
        res.write(`data: ${JSON.stringify({
          type: 'response.output_item.added',
          output_index: 0,
          item: {
            id: `${responseId}_item_0`,
            type: 'message',
            role: 'assistant',
            content: []
          }
        })}\n\n`);

        let fullText = '';
        let buffer = '';

        proxyRes.on('data', (chunk) => {
          buffer += chunk.toString();
          const lines = buffer.split('\n');
          buffer = lines.pop() || '';

          for (const line of lines) {
            const trimmed = line.trim();
            if (!trimmed || !trimmed.startsWith('data: ')) continue;
            const jsonStr = trimmed.slice(6);
            if (jsonStr === '[DONE]') continue;

            try {
              const chunk = JSON.parse(jsonStr);
              const delta = chunk.choices?.[0]?.delta || {};
              const content = delta.content || '';
              const reasoning = delta.reasoning_content || '';

              if (reasoning) {
                res.write(`data: ${JSON.stringify({
                  type: 'response.reasoning_text.delta',
                  output_index: 0,
                  delta: reasoning
                })}\n\n`);
              }

              if (content) {
                fullText += content;
                res.write(`data: ${JSON.stringify({
                  type: 'response.output_text.delta',
                  output_index: 0,
                  content_index: 0,
                  delta: content
                })}\n\n`);
              }
            } catch (e) {}
          }
        });

        proxyRes.on('end', () => {
          // Send output_text.done
          res.write(`data: ${JSON.stringify({
            type: 'response.output_text.done',
            output_index: 0,
            content_index: 0,
            text: fullText
          })}\n\n`);

          // Send output_item.done
          res.write(`data: ${JSON.stringify({
            type: 'response.output_item.done',
            output_index: 0,
            item: {
              id: `${responseId}_item_0`,
              type: 'message',
              role: 'assistant',
              content: [{ type: 'output_text', text: fullText }]
            }
          })}\n\n`);

          // Send response.completed
          res.write(`data: ${JSON.stringify({
            type: 'response.completed',
            response: {
              id: responseId,
              object: 'response',
              created_at: Math.floor(Date.now() / 1000),
              model: data.model || provider.models[0],
              output: [{
                id: `${responseId}_item_0`,
                type: 'message',
                role: 'assistant',
                content: [{ type: 'output_text', text: fullText }]
              }],
              status: 'completed'
            }
          })}\n\n`);

          res.write('data: [DONE]\n\n');
          res.end();

          // Track usage for streaming (estimate from text length)
          const model = data.model || provider.models[0];
          const estTokens = Math.ceil(fullText.length / 3);
          trackUsage(config.activeProvider, model, { prompt_tokens: 0, completion_tokens: estTokens, total_tokens: estTokens });
        });
      } else {
        // Non-streaming
        let body = '';
        proxyRes.on('data', chunk => body += chunk);
        proxyRes.on('end', () => {
          try {
            const respData = JSON.parse(body);

            // Track usage
            if (respData.usage) {
              trackUsage(config.activeProvider, respData.model || data.model || provider.models[0], respData.usage);
            }

            const response = {
              id: respData.id || '',
              object: 'response',
              created: respData.created || 0,
              model: respData.model || '',
              output: [{
                type: 'message',
                role: 'assistant',
                content: [{
                  type: 'output_text',
                  text: respData.choices?.[0]?.message?.content || ''
                }]
              }]
            };
            res.json(response);
          } catch (e) {
            res.status(502).json({ error: 'Invalid response from provider' });
          }
        });
      }
    });

    proxyReq.on('error', (e) => {
      res.status(502).json({ error: `Proxy error: ${e.message}` });
    });

    proxyReq.write(JSON.stringify(chatData));
    proxyReq.end();
  });

  return proxyApp;
}

