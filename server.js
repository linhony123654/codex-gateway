// JWT secret
function getJwtSecret() {
  try {
    return fs.readFileSync(JWT_SECRET, 'utf8').trim();
  } catch {
    const secret = require('crypto').randomBytes(32).toString('hex');
    fs.writeFileSync(JWT_SECRET, secret);
    return secret;
  }
}

