// Delete provider
app.delete('/api/providers/:id', authMiddleware, (req, res) => {
  const config = loadConfig();
  const { type } = req.query; // 'codex' or 'claude'
  const p = config.providers[req.params.id];
  if (!p) return res.status(404).json({ error: 'Provider not found' });

  if (type === 'codex') {
    // Only remove Codex config
    delete p.baseUrl;
    delete p.models;
    if (config.activeCodex === req.params.id) config.activeCodex = null;
  } else if (type === 'claude') {
    // Only remove Claude config
    delete p.anthropicUrl;
    delete p.claudeModel;
    if (config.activeClaude === req.params.id) config.activeClaude = null;
  }

  // If both configs are gone, remove the provider entirely
  if (!p.baseUrl && !p.anthropicUrl) {
    delete config.providers[req.params.id];
    if (config.activeProvider === req.params.id) config.activeProvider = null;
  }

  saveConfig(config);
  res.json({ success: true });
});

