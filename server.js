const USAGE_PATH = path.join(process.env.HOME, '.codex-gateway', 'usage.json');

function loadUsage() {
  try {
    return JSON.parse(fs.readFileSync(USAGE_PATH, 'utf8'));
  } catch {
    return { providers: {}, daily: [], total: { requests: 0, promptTokens: 0, completionTokens: 0, totalTokens: 0 } };
  }
}

function saveUsage(usage) {
  fs.writeFileSync(USAGE_PATH, JSON.stringify(usage, null, 2));
}

function trackUsage(providerId, model, usage) {
  const data = loadUsage();
  const today = new Date().toISOString().slice(0, 10);

  // Provider stats
  if (!data.providers[providerId]) {
    data.providers[providerId] = { models: {}, total: { requests: 0, promptTokens: 0, completionTokens: 0, totalTokens: 0 } };
  }
  const prov = data.providers[providerId];
  if (!prov.models[model]) {
    prov.models[model] = { requests: 0, promptTokens: 0, completionTokens: 0, totalTokens: 0 };
  }
  prov.models[model].requests++;
  prov.models[model].promptTokens += usage.prompt_tokens || 0;
  prov.models[model].completionTokens += usage.completion_tokens || 0;
  prov.models[model].totalTokens += usage.total_tokens || 0;
  prov.total.requests++;
  prov.total.promptTokens += usage.prompt_tokens || 0;
  prov.total.completionTokens += usage.completion_tokens || 0;
  prov.total.totalTokens += usage.total_tokens || 0;

  // Daily stats
  if (!data.daily) data.daily = [];
  let dayEntry = data.daily.find(d => d.date === today);
  if (!dayEntry) {
    dayEntry = { date: today, requests: 0, promptTokens: 0, completionTokens: 0, totalTokens: 0, providers: {} };
    data.daily.push(dayEntry);
  }
  dayEntry.requests++;
  dayEntry.promptTokens += usage.prompt_tokens || 0;
  dayEntry.completionTokens += usage.completion_tokens || 0;
  dayEntry.totalTokens += usage.total_tokens || 0;
  if (!dayEntry.providers[providerId]) {
    dayEntry.providers[providerId] = { requests: 0, promptTokens: 0, completionTokens: 0, totalTokens: 0 };
  }
  dayEntry.providers[providerId].requests++;
  dayEntry.providers[providerId].promptTokens += usage.prompt_tokens || 0;
  dayEntry.providers[providerId].completionTokens += usage.completion_tokens || 0;
  dayEntry.providers[providerId].totalTokens += usage.total_tokens || 0;

  // Keep last 90 days
  if (data.daily.length > 90) data.daily = data.daily.slice(-90);

  // Total
  data.total.requests++;
  data.total.promptTokens += usage.prompt_tokens || 0;
  data.total.completionTokens += usage.completion_tokens || 0;
  data.total.totalTokens += usage.total_tokens || 0;

  saveUsage(data);
}

